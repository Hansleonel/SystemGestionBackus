    private void Tbl_UPS_Datos_MostrandoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tbl_UPS_Datos_MostrandoMouseClicked
        // TODO add your handling code here:
        
        //Recordar completar el codigo con el valor dentro de la fila a la cual se ace referencia
        //la columna es 0 es decir no varia puesto que usaremos siempre la primera columna como referencia
        
        String buscar;
        int fila = Tbl_UPS_Datos_Mostrando.rowAtPoint(evt.getPoint());
        buscar = Tbl_UPS_Datos_Mostrando.getValueAt(fila, 0).toString();
        UbicarPuntoUPS=buscar;
        
        String propietario;
        propietario = Tbl_UPS_Datos_Mostrando.getValueAt(fila, 19).toString();
        if(propietario.equals("CERVECERIA SAN JUAN")){
            FrmGestion_DSP_Datos_Detalle_S frm_dps_detalle_s = new FrmGestion_DSP_Datos_Detalle_S();
            frm_dps_detalle_s.setVisible(true);
            frm_dps_detalle_s.toFront();
        }
        else if(propietario.equals("BACKUS")){
            FrmGestion_DSP_Datos_Detalle_Backus frm_dps_detalle_backus = new FrmGestion_DSP_Datos_Detalle_Backus();
            frm_dps_detalle_backus.setVisible(true);
            frm_dps_detalle_backus.toFront();
        }
        else if(propietario.equals("TRANSPORTES 77")){
            FrmGestion_DSP_Datos_Detalle_T frm_dps_detalle_t = new FrmGestion_DSP_Datos_Detalle_T();
            frm_dps_detalle_t.setVisible(true);
            frm_dps_detalle_t.toFront();
        }
        dispose();
    }//GEN-LAST:event_Tbl_UPS_Datos_MostrandoMouseClicked

