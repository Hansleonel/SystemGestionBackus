public class D_Mail {
    private String User;
    private String Password;
    private String ArchiveDir;
    private String ArchiveName;
    private String Destine;
    private String Asunto;
    private String Mensaje;

    /**
     * @return the User
     */
    public String getUser() {
        return User;
    }

    /**
     * @param User the User to set
     */
    public void setUser(String User) {
        this.User = User;
    }

    /**
     * @return the Password
     */
    public String getPassword() {
        return Password;
    }

    /**
     * @param Password the Password to set
     */
    public void setPassword(String Password) {
        this.Password = Password;
    }

    /**
     * @return the ArchiveDir
     */
    public String getArchiveDir() {
        return ArchiveDir;
    }

    /**
     * @param ArchiveDir the ArchiveDir to set
     */
    public void setArchiveDir(String ArchiveDir) {
        this.ArchiveDir = ArchiveDir;
    }

    /**
     * @return the ArchiveName
     */
    public String getArchiveName() {
        return ArchiveName;
    }

    /**
     * @param ArchiveName the ArchiveName to set
     */
    public void setArchiveName(String ArchiveName) {
        this.ArchiveName = ArchiveName;
    }

    /**
     * @return the Destine
     */
    public String getDestine() {
        return Destine;
    }

    /**
     * @param Destine the Destine to set
     */
    public void setDestine(String Destine) {
        this.Destine = Destine;
    }

    /**
     * @return the Asunto
     */
    public String getAsunto() {
        return Asunto;
    }

    /**
     * @param Asunto the Asunto to set
     */
    public void setAsunto(String Asunto) {
        this.Asunto = Asunto;
    }

    /**
     * @return the Mensaje
     */
    public String getMensaje() {
        return Mensaje;
    }

    /**
     * @param Mensaje the Mensaje to set
     */
    public void setMensaje(String Mensaje) {
        this.Mensaje = Mensaje;
    }
    
}
